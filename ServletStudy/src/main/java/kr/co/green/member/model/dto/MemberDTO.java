package kr.co.green.member.model.dto;

public class MemberDTO {
	private String name;
	private String id;
	private String pwd;
	
	// Alt + Shift + S
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	
}
